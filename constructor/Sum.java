package com.constructor;

public class Sum {
	
	Sum(int a, int b){
		
		int sum = a+b;
		System.out.println("Sum of a and b is : "+ sum);
		
	}
	
	
	Sum(int a, float b){
		
		float sum = a+b;
		System.out.println("Sum of a and b is : "+ sum);
		
	}
	
	
	Sum(int a, int b , int c){
		
		int sum = a+b+c;
		System.out.println("Sum of a and b is : "+ sum);
		
		
	}

}
